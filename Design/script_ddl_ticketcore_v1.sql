CREATE TABLE USU_usuario (
    usu_id_usuario   BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1000000001) PRIMARY KEY,
    usu_nm_usuario   VARCHAR(150) NOT NULL,
    usu_ds_email     VARCHAR(255) NOT NULL UNIQUE,
    usu_ds_senha     VARCHAR(255) NOT NULL,
    usu_nu_cpf_cnpj  VARCHAR(14)  NOT NULL UNIQUE,
    usu_ds_foto_perfil TEXT,
    usu_dt_cadastro  TIMESTAMP    DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE PER_perfil (
    per_id_perfil  BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1000000001) PRIMARY KEY,
    per_nm_perfil  VARCHAR(50) NOT NULL UNIQUE
);

CREATE TABLE USP_usuario_perfil (
    usp_id_usuario BIGINT NOT NULL,
    usp_id_perfil  BIGINT NOT NULL,
    PRIMARY KEY (usp_id_usuario, usp_id_perfil),
    FOREIGN KEY (usp_id_usuario) REFERENCES USU_usuario(usu_id_usuario) ON DELETE CASCADE,
    FOREIGN KEY (usp_id_perfil)  REFERENCES PER_perfil(per_id_perfil)   ON DELETE CASCADE
);

CREATE TABLE CAT_categoria (
    cat_id_categoria BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1000000001) PRIMARY KEY,
    cat_nm_categoria VARCHAR(100) NOT NULL,
    cat_ds_categoria TEXT
);

CREATE TABLE EVE_evento (
    eve_id_evento    BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1000000001) PRIMARY KEY,
    eve_id_organizador BIGINT  NOT NULL,
    eve_id_categoria   BIGINT  NOT NULL,
    eve_nm_evento    VARCHAR(200) NOT NULL,
    eve_dt_evento    TIMESTAMP   NOT NULL,
    eve_nm_local     VARCHAR(200) NOT NULL,
    eve_qt_capacidade INTEGER     NOT NULL CHECK (eve_qt_capacidade > 0),
    eve_ds_imagem    TEXT,
    FOREIGN KEY (eve_id_organizador) REFERENCES USU_usuario(usu_id_usuario),
    FOREIGN KEY (eve_id_categoria)   REFERENCES CAT_categoria(cat_id_categoria)
);

CREATE TABLE TPI_tipo_ingresso (
    tpi_id_tipo_ingresso BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1000000001) PRIMARY KEY,
    tpi_id_evento        BIGINT         NOT NULL,
    tpi_nm_tipo          VARCHAR(100)   NOT NULL,
    tpi_vl_preco         NUMERIC(10,2)  NOT NULL CHECK (tpi_vl_preco >= 0),
    tpi_qt_lote          INTEGER        NOT NULL CHECK (tpi_qt_lote > 0),
    FOREIGN KEY (tpi_id_evento) REFERENCES EVE_evento(eve_id_evento) ON DELETE CASCADE
);

CREATE TABLE TRA_transacao (
    tra_id_transacao  BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1000000001) PRIMARY KEY,
    tra_id_comprador  BIGINT        NOT NULL,
    tra_dt_transacao  TIMESTAMP     DEFAULT CURRENT_TIMESTAMP,
    tra_st_transacao  CHAR(1)       NOT NULL DEFAULT 'P' CHECK (tra_st_transacao IN ('C', 'P', 'X')),
    tra_vl_total      NUMERIC(10,2) NOT NULL CHECK (tra_vl_total >= 0),
    FOREIGN KEY (tra_id_comprador) REFERENCES USU_usuario(usu_id_usuario)
);

CREATE TABLE ING_ingresso (
    ing_id_ingresso      BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1000000001) PRIMARY KEY,
    ing_id_transacao     BIGINT      NOT NULL,
    ing_id_tipo_ingresso BIGINT      NOT NULL,
    ing_cd_acesso        VARCHAR(50) NOT NULL UNIQUE,
    ing_st_ingresso      CHAR(1)     NOT NULL DEFAULT 'A' CHECK (ing_st_ingresso IN ('A', 'U', 'C')),
    FOREIGN KEY (ing_id_transacao)     REFERENCES TRA_transacao(tra_id_transacao),
    FOREIGN KEY (ing_id_tipo_ingresso) REFERENCES TPI_tipo_ingresso(tpi_id_tipo_ingresso)
);

CREATE TABLE NOT_notificacao (
    not_id_notificacao BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1000000001) PRIMARY KEY,
    not_id_usuario     BIGINT    NOT NULL,
    not_ds_mensagem    TEXT      NOT NULL,
    not_ch_lida        CHAR(1)   NOT NULL DEFAULT 'N' CHECK (not_ch_lida IN ('S', 'N')),
    not_dt_criacao     TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (not_id_usuario) REFERENCES USU_usuario(usu_id_usuario) ON DELETE CASCADE
);


